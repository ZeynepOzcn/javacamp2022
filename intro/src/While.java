public class While {
    public static void main(String[] args) {
        int i = 1;
        while (i < 10) {
            System.out.println(i);
            i++;
        }
        System.out.println("While  bitti.");
        int j = 0;
        do {
            System.out.println(j);
            j += 2;
        } while (j < 10);
        System.out.println("While  bitti.");
    }

}
